package Exercise41;

public class Date {
	private int day;
	private int month;
	private int year;
/**
 * this is constructor of class Date. There are 3 field
 * @param day
 * @param month
 * @param year
 * example:
 *  new Date(1, 6, 2005);
 */
	public Date(int day, int month, int year) {
		this.day = day;
		this.month = month;
		this.year = year;
	}
	

}
